// Función para sumar dos números
const sumar = (a: number, b: number): number => {
    return a + b;
};

// Función para restar dos números
const restar = (a: number, b: number): number => {
    return a - b;
};

// Pruebas rápidas
console.log("Suma:", sumar(10, 5));
console.log("Resta:", restar(10, 5));